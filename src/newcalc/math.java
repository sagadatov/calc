package newcalc;

public class math {
    
    public static int sum(int a, int b) {
        int sum = a+b;
        return sum;
    }
    
    public static int div(int a, int b) {
        int div = a/b;
        return div;
    }
    
    public static int sub(int a, int b) {
        int sub = a-b;
        return sub;
    }
    
    public static int mult(int a, int b) {
        int mult = a*b;
        return mult;
    }
}
